// Grid cell structure
// holds positions and values for the 8 corners
struct GridCell
{
    vec3 pos[8];
    float val[8];
};

// Simple triangle structure
struct Triangle
{
    vec3 p[3];
};

int TriangulateCell(const GridCell& cell, float iso, Triangle* tris);
vec3 VertexInterpolate(float iso, const vec3& p0, const vec3& p1, float v0, float v1);

inline int idx(const ivec3& res, const ivec3& coord) {
    return res.y * res.x * coord.z + res.x * coord.y + coord.x;
}

bool Triangulate(Mesh* mesh,
    const UniformGridVolume& volume,
    float iso,
    const AABB& bounds)
{
    // Check against nullpointer
    if (!mesh) {
        printf("No mesh was supplied \n");
        return false;
    }

    // Reserve some vertices and triangles for the mesh
    mesh->vertices.reserve(4096);
    mesh->triangles.reserve(1024);

    // Create some convenient variables
    const float* data = volume.GetData();
    const ivec3 res = volume.GetRes();
    const vec3 idx_to_pos_scl = bounds.Size() / vec3(res - 1);

    Triangle tris[5];
    GridCell cell;

    // Iterate over all "cells"
    for (int z = 0; z < res.z - 1; ++z) {
        for (int y = 0; y < res.y - 1; ++y) {
            for (int x = 0; x < res.x - 1; ++x) {
                // Calculate positions for the corners of the cell
                cell.pos[0] = vec3(x + 0, y + 0, z + 0) * idx_to_pos_scl + bounds.min;
                cell.pos[1] = vec3(x + 1, y + 0, z + 0) * idx_to_pos_scl + bounds.min;
                cell.pos[2] = vec3(x + 1, y + 1, z + 0) * idx_to_pos_scl + bounds.min;
                cell.pos[3] = vec3(x + 0, y + 1, z + 0) * idx_to_pos_scl + bounds.min;
                cell.pos[4] = vec3(x + 0, y + 0, z + 1) * idx_to_pos_scl + bounds.min;
                cell.pos[5] = vec3(x + 1, y + 0, z + 1) * idx_to_pos_scl + bounds.min;
                cell.pos[6] = vec3(x + 1, y + 1, z + 1) * idx_to_pos_scl + bounds.min;
                cell.pos[7] = vec3(x + 0, y + 1, z + 1) * idx_to_pos_scl + bounds.min;

                // Get intensity values at the corners of the cell
                cell.val[0] = data[idx(res, ivec3(x + 0, y + 0, z + 0))];
                cell.val[1] = data[idx(res, ivec3(x + 1, y + 0, z + 0))];
                cell.val[2] = data[idx(res, ivec3(x + 1, y + 1, z + 0))];
                cell.val[3] = data[idx(res, ivec3(x + 0, y + 1, z + 0))];
                cell.val[4] = data[idx(res, ivec3(x + 0, y + 0, z + 1))];
                cell.val[5] = data[idx(res, ivec3(x + 1, y + 0, z + 1))];
                cell.val[6] = data[idx(res, ivec3(x + 1, y + 1, z + 1))];
                cell.val[7] = data[idx(res, ivec3(x + 0, y + 1, z + 1))];

                int num_tris = TriangulateCell(cell, iso, tris);
                for (int i = 0; i < num_tris; ++i) {
                    int offset = (int)mesh->vertices.size();
                    Mesh::Vertex v;
                    Mesh::Triangle t;

                    // TODO: Compute Normal n from triangle tris[i]
                    vec3 n(0, 0, 1);
                    vec3 A = tris[i].p[1] - tris[i].p[0];
                    vec3 B = tris[i].p[2] - tris[i].p[0];
                    n.x = A.y * B.z - A.z * B.y;
                    n.y = A.z * B.x - A.x * B.z;
                    n.z = A.x * B.y - A.y * B.x;
                    n = glm::normalize(n);

                    // Add vertices and triangle to mesh    
                    for (int u = 0; u < 3; ++u) {
                        v.position = tris[i].p[u];
                        v.normal = n;
                        t.index[u] = offset + u;
                        mesh->vertices.push_back(v);
                    }
                    mesh->triangles.push_back(t);
                }
            }
        }
    }

    // Return success
    return true;
}

int TriangulateCell(const GridCell& cell, float iso, Triangle* tris)
{
    int caseIndex = 0;

    // TODO: Compute case index by comparing cell.val[0..7] against iso
    for (int i = 0; i < 8; i++)
    {
        caseIndex = caseIndex + (cell.val[i] < iso) * (pow(2, i));
    }
    ;

    // Check against the two trivial cases: completely inside or outside
    if (caseIndex == 0 || caseIndex == 255)
        return 0;

    // Get the intersection case from table
    int* intersectionCase = intersectionTable[caseIndex];

    // TODO: Create Triangles using intersectionCase
    // Hint: Use edgeTable as a lookup to get the index
    // to the corners which are shared by an edge

    int curTriangle = 0;
    while (intersectionCase[curTriangle * 3] > -1)
    {
        for (int curVertex = 0; curVertex < 3; curVertex++)
        {
            int edgeIndex = intersectionCase[curTriangle * 3 + curVertex];
            int v0_index = edgeTable[edgeIndex][0];
            int v1_index = edgeTable[edgeIndex][1];
            vec3 inter_pos = VertexInterpolate(iso,
                cell.pos[v0_index], cell.pos[v1_index],
                cell.val[v0_index], cell.val[v1_index]);
            tris[curTriangle].p[curVertex] = inter_pos;

        }
        curTriangle++;
    }
    // TODO: make sure this returns the number of triangles!!
    return curTriangle;
}

vec3 VertexInterpolate(float iso, const vec3& p0, const vec3& p1, float v0, float v1)
{
    // TODO: Interpolate a resulting vertex position using input positions and values
    float alpha = (iso - v0) / (v1 - v0);
    return p1 * alpha + p0 * (1 - alpha);
}